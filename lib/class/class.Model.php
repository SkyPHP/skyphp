    /**
     * makes a foreign key array from the aql array
     * used to figure out in what order saves will happen if there are joins
     * @param array $aql_array
     * @return array
     */
    public function makeFKArray($aql_array)
    {
        $fk = array();
        foreach ($aql_array as $k => $v) {
            if (is_array($v['fk'])) {
