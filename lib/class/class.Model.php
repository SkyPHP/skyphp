     * @return string | null
     */
    public function getIDE()
    {
        $field = $this->getPrimaryTable() . '_id';
        $field_ide = $field . 'e';

        return ($this->{$field_ide})
            ?: (($this->{$field})
                ? encrypt($this->{$field}, $this->_primary_table)
                : null);
    }

