    /**
     * @return int | null
     */
    public function getID()
    {
        $field = $this->getPrimaryTable() . '_id';
        $field_ide = $field . 'e' ;

        return $this->{$field} = ($this->{$field})
            ?: (($this->{$field_ide})
                ? decrypt($this->{$field_ide}, $this->_primary_table)
                : null);
    }

